//Include four operations

	//1. Add (number1, number 2)return result
	//2. Subtract (number1, number 2)return result
	//3. Multiply (number1, number 2)return result
	//4. Divide (number1, number 2)return result
	

public class Calculator {
	
	//Include four operations
	
		//1. Add (number1, number 2)return result
	
		public int add(int num1 , int num2) {
			return num1+num2;
			
		}
		//2. Subtract (number1, number 2)return result
		
		public int subtract(int num1 , int num2) {
			return num1-num2;
			
		}
		
		//3. Multiply (number1, number 2)return result
		
		public int multiply(int num1 , int num2) {
			return num1*num2;
			
		}
		//4. Divide (number1, number 2)return result
		
		public int divide(int num1 , int num2) {
			return num1/num2;
			
		}

}
